
package Apuesta;
public class Futbolista {
    
      private String nombre;
      private int ma1;
      private int ma2;

    public Futbolista(String nombre, int ma1, int ma2) {
        this.nombre = nombre;
        this.ma1 = ma1;
        this.ma2 = ma2;
    }

    public String getNombre() {
        return nombre;
    }

    public int getMa1() {
        return ma1;
    }

    public int getMa2() {
        return ma2;
    }
      
      
}
